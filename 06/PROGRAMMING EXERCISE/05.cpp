// 그 나라의 소득세율은 다음과 같이 정해져 있다.
/*
5000 트바프까지는 : 0%
그 다음 10000 트바프까지는 : 10%
또 그 다음 20000 트바프까지는 : 15%
35000 트바프 이상에 대해서는 : 20%
*/
// 즉, 35000 트바프의 소득이 있는 사람은 5000×0.00+10000×0.10+200000×0.15+3000×0.20 = 4600트바프를 세금으로 내야 한다. 
