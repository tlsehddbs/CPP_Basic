// 고객 A는 단리 10%로 100,000원을 투자하였다. 
// 따라서, 고객 A는 매년 원금의 10%인 10,000원을 이자 수익으로 얻는다.
/* 이자 수익 = 0.10 X 원금 */
// 같은 기간 동안에 고객 B는 복리 5%로 100,000원을 투자하였다. 
// 따라서, 고객 b가 매년 얻는 이자 수익은 전년도 원리금의 5%이다.
/* 이자 수익 = 0.05 X 전년도 원리금 */
// 고객 B는 1년 후 100,000원의 5%인 5,000원을 이자 수익으로 얻게 되므로 원리금이 105,000원이 된다. 
// 2년 후에는 전년도 원히금 105,000원의 5%인 5,250원을 이자 수익으로 얻게 된다. 
// 고객 B의 투자 가치가 고객 A의 투자 가치를 초과하는 년 수를 계산하고, 그때 두 사람의 투자 가치를 함께 출력하는 프로그램을 작성하라.
